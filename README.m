# 🎨 Creative Studio - About Us Page

A modern, responsive "About Us" landing page built using **HTML** and **SCSS**.

## 🚀 Features

- Clean SCSS architecture with mixins and media queries
- Fully responsive layout
- Minimal and elegant design

## 🛠 Tech Stack

- HTML5
- SCSS (compiled to CSS)

## ▶️ How to Run

1. Compile SCSS:

   ```bash
   sass scss/style.scss css/style.css

2. Open index.html in your browser.
